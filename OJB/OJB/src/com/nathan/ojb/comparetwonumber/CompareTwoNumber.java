package com.nathan.ojb.comparetwonumber;

/**
 * CompareTwoNumber: Tue,20150908
 * 
 * @author Nathan
 */
public class CompareTwoNumber {

	public static void main(String[] args) {
		float numberOne = 10;
		float numberTwo = 20;

		if (numberOne > numberTwo) {
			System.out.println(numberOne + " > " + numberTwo);
		}
		if (numberOne < numberTwo) {
			System.out.println(numberOne + " < " + numberTwo);
		}
		if (numberOne == numberTwo) {
			System.out.println(numberOne + " = " + numberTwo);
		}
	}

}
